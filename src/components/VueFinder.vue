<template>
  <div class="vuefinder" ref="root">
    <div :class="app.theme.actualValue">
      <div
          :class="app.fullscreen ? 'fixed w-screen inset-0 z-20' : 'relative rounded-md'"
          :style="!app.fullscreen ? 'height: 100%; width: 100%;' : ''"
          class="border flex flex-col bg-white dark:bg-gray-800 text-gray-700 dark:text-neutral-400 border-neutral-300 dark:border-gray-900 min-w-min select-none"
          @mousedown="app.emitter.emit('vf-contextmenu-hide')"
          @touchstart="app.emitter.emit('vf-contextmenu-hide')">
        <Toolbar/>
        <Breadcrumb/>
        <Explorer/>
        <Statusbar/>
      </div>

      <Transition name="fade">
        <Component v-if="app.modal.visible" :is="app.modal.type"/>
      </Transition>

      <ContextMenu/>
    </div>
  </div>
</template>

<script setup>
import {inject, onMounted, provide, ref, defineExpose} from 'vue';
import ServiceContainer from "../ServiceContainer.js";

import Toolbar from '../components/Toolbar.vue';
import Breadcrumb from '../components/Breadcrumb.vue';
import Explorer from '../components/Explorer.vue';
import ContextMenu from '../components/ContextMenu.vue';
import Statusbar from '../components/Statusbar.vue';

const emit = defineEmits(['select'])

const props = defineProps({
  id: {
    type: String,
    default: 'vf'
  },
  request: {
    type: [String, Object],
    required: true,
  },
  persist: {
    type: Boolean,
    default: false,
  },
  path: {
    type: String,
    default: '',
  },
  features: {
    type: [Array, Boolean],
    default: true,
  },
  debug: {
    type: Boolean,
    default: false,
  },
  theme: {
    type: String,
    default: 'system',
  },
  locale: {
    type: String,
    default: null
  },
  maxHeight: {
    type: String,
    default: '600px'
  },
  maxFileSize: {
    type: String,
    default: '10mb'
  },
  fullScreen: {
    type: Boolean,
    default: false
  },
  showThumbnails: {
    type: Boolean,
    default: true
  },
  selectButton: {
    type: Object,
    default(rawProps) {
      return {
        active: false,
        multiple: false,
        click: (items) => {
          // items is an array of selected items
          // 
        },
        ...rawProps,
      }
    },
  },
});

// the object is passed to all components as props
const app = ServiceContainer(props, inject('VueFinderOptions'));
provide('ServiceContainer', app);
const {setStore} = app.storage;

//  Define root element
const root = ref(null);
app.root = root;

// Define dragSelect object
const ds = app.dragSelect;

const updateItems = (data) => {
  Object.assign(app.fs.data, data);
  ds.clearSelection();
  ds.refreshSelection();
};

/** @type {AbortController} */
let controller;
app.emitter.on('vf-fetch-abort', () => {
  controller.abort();
  app.fs.loading = false;
});

// Fetch data
app.emitter.on('vf-fetch', ({params, body = null, onSuccess = null, onError = null, noCloseModal = false}) => {
  if (['index', 'search'].includes(params.q)) {
    if (controller) {
      controller.abort();
    }
    app.fs.loading = true;
  }

  controller = new AbortController();
  const signal = controller.signal;
  app.requester.send({
    url: '',
    method: params.m || 'get',
    params,
    body,
    abortSignal: signal,
  }).then(data => {
    app.fs.adapter = data.adapter;
    if (app.persist) {
      app.fs.path = data.dirname;
      setStore('path', app.fs.path);
    }

    if (['index', 'search'].includes(params.q)) {
      app.fs.loading = false;
    }
    if (!noCloseModal) {
      app.modal.close();
    }
    if (params.q === 'index') {
      if (data.sessionKey) {
        window.vueFinderSessionKey = data.sessionKey;
        window.vueFinderSessionKeyDate = new Date();
      }
    }
    updateItems(data);
    if (onSuccess) {
      onSuccess(data);
    }
  }).catch((e) => {
    console.error(e)
    if (onError) {
      onError(e);
    }
  });
});

// Download
app.emitter.on('vf-download', (url) => {
  // const $a = document.createElement('a');
  // $a.style.display = 'none';
  // $a.target = '_blank';
  // $a.href = url;
  // // Cross-origin this doesn't work, but at least this does bring up a new window.
  // $a.download = url;
  // app.root.appendChild($a);
  // $a.click();
  // $a.remove();
  console.log('download')
});

// fetch initial data
onMounted(() => {
  // app.fs.adapter can be null at first, until we get the adapter list it will be the first one from response
  // later we can set default adapter from a prop value

  // if there is a path coming from the prop, we should use it.
  let pathExists = {};

  if (app.fs.path.includes("://")) {
    pathExists = {
      adapter: app.fs.path.split("://")[0],
      path: app.fs.path
    };
  }

  app.emitter.emit('vf-fetch', {params: {q: 'index', adapter: app.fs.adapter, ...pathExists}});

  // Emit select event
  ds.onSelect((items) => {
    emit('select', items);
  });

});

defineExpose({
  app
});
</script>
